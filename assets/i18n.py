# ── Singleton state ───────────────────────────────────────────

_current_lang = "en"
_en = STRINGS["en"]


def detect_system_language() -> str:
    """Try to detect system locale and return matching language code."""
    try:
        loc = locale.getdefaultlocale()[0] or ""
        code = loc[:2].lower()
        if code in STRINGS:
            return code
    except Exception:
        pass
    return "en"


def set_language(code: str) -> None:
    global _current_lang
    if code in STRINGS:
        _current_lang = code


def get_language() -> str:
    return _current_lang


def t(key: str, **kwargs) -> str:
    """
    Get a translated string. Falls back to English if key missing.
    Supports format substitution: t("key", name="value")
    """
    lang_dict = STRINGS.get(_current_lang, {})
    text = lang_dict.get(key) or _en.get(key, key)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text


def language_options() -> list:
    """Returns list of (code, display_name) tuples for Settings."""
    result = []
    for code, strings in STRINGS.items():
        flag = strings.get("lang_flag", "")
        name = strings.get("lang_name", code.upper())
        result.append((code, f"{flag}  {name}"))
    return result


def display_name_to_code(display: str) -> str:
    for code, strings in STRINGS.items():
        flag = strings.get("lang_flag", "")
        name = strings.get("lang_name", code.upper())
        if display.strip() == f"{flag}  {name}".strip():
            return code
    return "en"
